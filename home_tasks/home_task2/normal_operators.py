a = 10
b = 5
print(f'{a} + {b} =', a + b)
print(f'{a} - {b} =', a - b)
print(f'{a} / {b} =', int(a / b))
print('a * b = {}'.format(a * b))
print('{} ** {} = {}'.format(a, b, a ** b))
print('{0} // {1} = {2}'.format(a, b, a // b))
print('{0} % {1} = {2}'.format(a, b, a % b))
print('{0} * {1} - 2 = {2}'.format(a, b, (a * b) - 2))


